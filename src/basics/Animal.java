package basics;

public class Animal  {
		public String type;
		public int size;
		public String sound;
		boolean wild;
		public String getType() {
			return type;
		}
		public void setType(String type) {
			this.type = type;
		}
		public int getSize() {
			return size;
		}
		public void setSize(int size) {
			this.size = size;
		}
		public String getSound() {
			return sound;
		}
		public void setSound(String sound) {
			this.sound = sound;
		}
		public boolean isWild() {
			return wild;
		}
		public void setWild(boolean wild) {
			this.wild = wild;
		}
}



	
	


